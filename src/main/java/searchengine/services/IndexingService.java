package searchengine.services;

/**
 * Интерфейс сервиса индексации сайтов.
 * Определяет основные методы для управления процессом индексации.
 */
public interface IndexingService {
    /**
     * Запускает полную индексацию всех сайтов, указанных в конфигурации.
     * @return true, если индексация успешно запущена, false если уже выполняется
     */
    boolean startIndexing();

    /**
     * Останавливает текущий процесс индексации.
     * @return true, если индексация успешно остановлена, false если не была запущена
     */
    boolean stopIndexing();

    /**
     * Индексирует отдельную страницу по указанному URL.
     * @param url URL страницы для индексации
     * @return true, если страница успешно проиндексирована, false в случае ошибки
     */
    boolean indexPage(String url);
}